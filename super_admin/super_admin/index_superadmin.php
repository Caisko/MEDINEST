    <script>
        feather.replace();
        const body = document.documentElement;
        let mainChart;
        let calendar;

        function applyTheme(theme) {
            body.setAttribute('data-bs-theme', theme);
            localStorage.setItem('admin_theme', theme);
            const icon = document.getElementById('themeIcon');
            if (icon) icon.outerHTML = `<i data-feather="${theme === 'dark' ? 'sun' : 'moon'}" id="themeIcon"></i>`;
            feather.replace();
            
            setTimeout(() => {
                initMainChart(theme);
                initSparklines(theme);
                if(calendar) calendar.render();
            }, 100);
        }

        function initCalendar() {
            const calendarEl = document.getElementById('calendar');
            if (!calendarEl) return;
            calendar = new FullCalendar.Calendar(calendarEl, {
                initialView: 'dayGridMonth',
                headerToolbar: { left: 'prev', center: 'title', right: 'next' },
                events: [{ title: 'Vaccination', start: new Date(), color: '#220bef' }],
                height: 'auto'
            });
            calendar.render();
        }

        function initMainChart(theme) {
            const isDark = theme === 'dark';
            const options = {
                chart: { type: 'bar', height: 350, toolbar: { show: false }, foreColor: isDark ? '#a0a0a0' : '#6c757d' },
                series: [
                    { name: 'Regular', data: [120, 155, 187, 156, 191, 210] },
                    { name: 'Emergency', data: [45, 60, 85, 42, 77, 95] }
                ],
                xaxis: { categories: ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun'] },
                colors: ['#220bef', isDark ? '#2d343f' : '#e2e8f0'],
                plotOptions: { bar: { columnWidth: '55%', borderRadius: 5 } },
                legend: { position: 'top' },
                tooltip: { theme: isDark ? 'dark' : 'light' }
            };
            if (mainChart) mainChart.destroy();
            mainChart = new ApexCharts(document.querySelector("#main-status-chart"), options);
            mainChart.render();
        }

        function initSparklines(theme) {
            const isDark = theme === 'dark';
            const ids = ['#chart-clinics', '#chart-clients', '#chart-legit', '#chart-pending'];
            const colors = ['#220bef', '#6366f1', '#10b981', '#ef4444'];
            
            ids.forEach((id, idx) => {
                const el = document.querySelector(id);
                if(!el) return;
                el.innerHTML = "";
                new ApexCharts(el, {
                    chart: { type: 'area', height: 80, sparkline: { enabled: true } },
                    stroke: { curve: 'smooth', width: 2 },
                    colors: [colors[idx]],
                    series: [{ data: [31, 40, 28, 51, 42, 109, 100] }],
                    fill: { type: 'gradient', gradient: { opacityFrom: 0.3, opacityTo: 0 } },
                    tooltip: { theme: isDark ? 'dark' : 'light' }
                }).render();
            });
        }

        document.getElementById('darkModeBtn').addEventListener('click', () => {
            const current = body.getAttribute('data-bs-theme');
            applyTheme(current === 'dark' ? 'light' : 'dark');
        });

        document.getElementById('mobileToggle').addEventListener('click', () => {
            document.getElementById('sidebar').classList.add('show');
            document.getElementById('overlay').classList.add('show');
        });

        document.getElementById('overlay').addEventListener('click', () => {
            document.getElementById('sidebar').classList.remove('show');
            document.getElementById('overlay').classList.remove('show');
        });

        document.addEventListener('DOMContentLoaded', () => {
            applyTheme(localStorage.getItem('admin_theme') || 'light');
            initCalendar();
        });
