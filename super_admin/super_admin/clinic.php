<script>
    feather.replace();
    const body = document.documentElement;
    const sidebar = document.getElementById('sidebar');
    const content = document.getElementById('main-content');
    const toggleBtn = document.getElementById('toggleSidebar');
    const darkModeBtn = document.getElementById('darkModeBtn');
    const themeIcon = document.getElementById('themeIcon');
    let lineChart;

    function applyTheme(theme) {
        body.setAttribute('data-bs-theme', theme);
        localStorage.setItem('admin_theme', theme);
        themeIcon.outerHTML = theme === 'dark' 
            ? '<i data-feather="sun" id="themeIcon"></i>' 
            : '<i data-feather="moon" id="themeIcon"></i>';
        feather.replace();
        initChart(theme);
    }

    darkModeBtn.addEventListener('click', () => {
        const currentTheme = body.getAttribute('data-bs-theme');
        applyTheme(currentTheme === 'dark' ? 'light' : 'dark');
    });

    toggleBtn.addEventListener('click', () => {
        if (window.innerWidth > 992) {
            sidebar.classList.toggle('sidebar-collapsed');
            content.classList.toggle('content-collapsed');
        } else {
            sidebar.classList.toggle('mobile-show');
        }
    });

    function initChart(theme) {
        const ctx = document.getElementById('lineChart').getContext('2d');
        if (lineChart) lineChart.destroy();
        const isDark = theme === 'dark';
        const gridColor = isDark ? 'rgba(255,255,255,0.05)' : '#f0f0f0';
        const textColor = isDark ? '#a0a0a0' : '#6c757d';

        lineChart = new Chart(ctx, {
            type: 'line',
            data: {
                labels: ['Jan','Feb','Mar','Apr','May','Jun'],
                datasets: [{
                    label: 'Clinics',
                    data: [10,25,15,30,45,60],
                    borderColor: '#220bef',
                    backgroundColor: 'rgba(34, 11, 239, 0.1)',
                    fill: true,
                    tension: 0.4,
                    borderWidth: 3
                }]
            },
            options: {
                responsive:true,
                maintainAspectRatio:false,
                plugins:{legend:{display:false}},
                scales:{
                    y:{grid:{color:gridColor},ticks:{color:textColor}},
                    x:{grid:{display:false},ticks:{color:textColor}}
                }
            }
        });
    }

    document.addEventListener('DOMContentLoaded', () => {
        const savedTheme = localStorage.getItem('admin_theme') || 'light';
        applyTheme(savedTheme);
    });

    // Modal document loader & approve/reject
    const reviewButtons = document.querySelectorAll('.reviewBtn');
    const approveBtn = document.querySelector('.approveBtn');
    const rejectBtn = document.querySelector('.rejectBtn');

    reviewButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            const clinicName = btn.dataset.clinic;
            const verification = btn.dataset.verification;
            const face = btn.dataset.face;
            const idvalidation = btn.dataset.idvalidation;
            const clinicId = btn.dataset.id;

            document.getElementById('modalClinicName').textContent = clinicName;

            approveBtn.dataset.id = clinicId;
            rejectBtn.dataset.id = clinicId;

            const modalDocuments = document.getElementById('modalDocuments');
            modalDocuments.innerHTML = '';

            const docs = [
                { name: 'Business/Verification File', file: '../../uploads/' + verification },
                { name: 'Face Authentication File', file: '../../uploads/' + face },
                { name: 'ID Validation File', file: '../../uploads/' + idvalidation }
            ];

            docs.forEach(doc => {
                const docContainer = document.createElement('div');
                docContainer.style.marginBottom = '15px';
                const label = document.createElement('p');
                label.textContent = doc.name;
                label.style.fontWeight = 'bold';
                docContainer.appendChild(label);
                const img = document.createElement('img');
                img.src = doc.file;
                img.alt = doc.name;
                img.style.maxWidth = '100%';
                img.style.border = '1px solid #ccc';
                img.style.borderRadius = '5px';
                img.style.padding = '5px';
                docContainer.appendChild(img);
                modalDocuments.appendChild(docContainer);
            });
        });
    });

    function updateClinicStatus(clinicId, status) {
        fetch('clinic_action.php', {
            method: 'POST',
            headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
            body: `id=${clinicId}&status=${status}`
        })
        .then(res => res.json())
        .then(data => {
            if(data.success){
                const row = document.querySelector(`button[data-id="${clinicId}"]`).closest('tr');
                row.remove();
                const modal = bootstrap.Modal.getInstance(document.getElementById('reviewModal'));
                modal.hide();
            } else {
                alert(data.message || 'Something went wrong.');
            }
        })
        .catch(err => console.error(err));
    }

    approveBtn.addEventListener('click', () => {
        const id = approveBtn.dataset.id;
        updateClinicStatus(id, 'approved');
    });

    rejectBtn.addEventListener('click', () => {
        const id = rejectBtn.dataset.id;
        updateClinicStatus(id, 'rejected');
    });
</script>
